//	то сума повинна дорівнювати 0. Для кожної групи вивести відповідну їй цифру і
//	знайдену суму, впорядковуючи пари, що виводяться за зростанням цифр.
//	Використовувати допоміжне відображення M та варіант угрупування, описаний у
